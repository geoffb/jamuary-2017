proto.moveEntity = function (entity, distance) {
  let dx = Math.cos(entity.direction) * distance;
  let dy = Math.sin(entity.direction) * distance;
  if (this.map.get(entity.x + dx, entity.y) === 0) {
    entity.x += dx;
  }
  if (this.map.get(entity.x, entity.y + dy) === 0) {
    entity.y += dy;
  }
};
